// ─────────────────────────────────────────────────────────────────────────────
// JEWISH WEDDINGS
// ─────────────────────────────────────────────────────────────────────────────
/*
Denominational differences first (Orthodox → most restricted, Reform → most flexible).
Never recommend music without establishing denomination. Rabbi often has strong
opinions — couple does not have complete freedom. Predominantly Ashkenazi in SA
(JHB and Cape Town communities).

8 ceremony moments:
1. Kabbalat Panim — pre-ceremony reception; soft instrumental or quiet vocal
2. Badeken (veiling) — emotional moment; very soft instrumental or silence
3. Processional to chuppah — groom escorted by parents first, then bride separately;
   distinct pieces for each. Traditional choices: "Erev Shel Shoshanim",
   "Dodi Li", "Yerushalayim Shel Zahav"
4. Bride circles groom — Hakafot — soft instrumental must continue the full 3–4 minutes
5. Kiddushin (betrothal blessings and ring) — solemn; soft music or silence
6. Ketubah reading — no music
7. Sheva Brachot (Seven Blessings) — music may introduce or conclude the blessings;
   "Im Eshkachech Yerushalayim" sung just before glass breaking
8. Glass breaking → recessional — THE most critical cue in any tradition.
   "Siman Tov u'Mazel Tov" must start THE INSTANT the glass shatters — zero delay.
   Even a two-second gap kills the energy. Band must be primed and watching.
   Immediately into "Od Yishama" and celebratory songs.
9. Yichud (couple's private moment) — music as they exit

Reception: The Hora is non-negotiable. "Hava Nagila" is the anchor.
Klezmer ensemble (clarinet, violin, accordion) is traditional for reception.
Modern SA Jewish weddings blend klezmer with guitar, bass, drums, saxophone.

Wagner: STRICTLY AVOIDED — flag automatically for any Jewish ceremony or
Jewish-Christian interfaith combination. Mendelssohn's Wedding March also
historically sensitive for Jewish-Christian combinations.

Most forgotten: music for the circling (Hakafot must not stop), Kabbalat Panim,
briefing musician specifically on glass-breaking cue, Yichud music.

Key planning requirement: hire musicians experienced with Jewish ceremony flow.
A band that misses the glass-breaking cue or plays through the Sheva Brachot
creates awkward moments that cannot be recovered.
*/

// ─────────────────────────────────────────────────────────────────────────────
// MUSLIM WEDDINGS (MAINSTREAM)
// ─────────────────────────────────────────────────────────────────────────────
/*
THE MOST CRITICAL RULE: The Nikah ceremony has NO music. None.
The Nikah is a solemn religious proceeding — only Qur'anic recitation,
supplications, and the Imam's sermon. Suggesting music for the Nikah will
cause offence and signals unfamiliarity with the tradition. The music
planner's role begins ONLY at the Walima.

Music permissibility varies widely across families and communities —
establish the family's level of observance before any recommendations.

Nikah ceremony moments (ALL NO MUSIC):
- Tolbe (formal request for bride's hand)
- Mehr (groom's gift to bride)
- Ijab-e-Qubul (consent — "qubool hai" x3)
- Nikah-Namah (signing of marriage contract)
- Khutbah (sermon) and Qur'anic recitation

Walima (reception) — first musical moment:
- Couple's entrance: nasheed or modest celebratory music
- Feast: nasheeds, daff drum, or (in some communities) broader music

Safest choices: Nasheeds (Maher Zain, Sami Yusuf, Harris J), daff drum
The daff (frame drum) is the only instrument widely considered permissible
by Islamic scholars. Stringed and wind instruments are discouraged or
forbidden under stricter interpretations. Confirm with the Imam.

Songs with vulgar lyrics, content promoting un-Islamic values, or excessively
romantic themes are forbidden at the Walima. Emphasis on joyful but modest
celebration. More conservative communities prefer nasheed only.

South Asian Muslim additions:
- Baraat (groom's procession): major music moment — dhol, Bollywood, 20 min to 1+ hour
- Rukhsat (bride's farewell): intensely emotional — frequently unplanned

Cape Malay — see separate section below. Always ask if couple identifies as
Cape Malay — it is an entirely different planning context.

Most forgotten: checking music permissibility with the Imam before planning
anything, planning Baraat separately for South Asian families, Rukhsat moment.
*/

