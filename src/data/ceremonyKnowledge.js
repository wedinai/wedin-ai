/*
Prelude music: 15–30 min as guests arrive — the MOST FREQUENTLY unplanned
moment across all traditions. Flag in every ceremony brief.

Seating of parents/grandmothers: a distinct music cue, frequently overlooked.
Must be planned and briefed to the act.

Signing of the register: SA legal requirement across all marriages. Takes
several minutes. Must be planned — frequently left to improvisation.

Officiant/clergy music constraints: establish early — a music plan built
without the officiant's input may be unusable on the day. This applies to
priests, ministers, rabbis, imams, dominees, and pandits equally.

Rehearsal: required for Jewish (glass cue), Hindu (pandit pacing), Catholic
(required sung elements), Orthodox (choir coordination). Flag in every
religious ceremony brief.

Live vs recorded: live music is strongly preferred for the ceremony across
nearly every tradition. Recorded tracks lack the flexibility to adjust to
real-time ceremony pacing and the emotional impact of live performance.
When recommending recorded, be honest about the tradeoff.

Family involvement: for Zulu, Xhosa, Cape Malay, and Hindu weddings,
the music is a family affair. Prompt couples to involve their families —
especially elders — in the music planning process.

Multilingual and multicultural: SA has 12 official languages. Incorporating
at least one hymn or song in a language other than English can make the
ceremony significantly more inclusive for guests.
*/

