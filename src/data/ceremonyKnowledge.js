// GREEK AND EASTERN ORTHODOX WEDDINGS
// ─────────────────────────────────────────────────────────────────────────────
/*
Most restrictive of any Christian tradition. NO instruments in the church.
Entire service is chanted or sung a cappella. Priest has absolute authority.
No external musicians are hired — music is handled entirely by the church
choir and/or chanter (psaltis).

Key moments: Opening Litany (Kyrie Eleison), The Crowning/Stefana,
Dance of Isaiah (couple processes around altar three times).

No sound equipment needed during the ceremony.
Early engagement with choir director is essential.
SA context: established Greek communities in JHB and Cape Town.

Reception music: entirely separate event, no restrictions.
*/

// ─────────────────────────────────────────────────────────────────────────────
// ZULU TRADITIONAL WEDDINGS (UMSHADO / UMABO)
// ─────────────────────────────────────────────────────────────────────────────
/*
FUNDAMENTAL SHIFT IN PLANNING FRAME: There is no band to hire and brief.
The music IS the families. The planner's role is to coordinate the two
families' repertoires, ensure rehearsal has happened, and ensure the
sequence is understood. Treat this like choreography, not a playlist.

No formal religious rules govern song selection. Instead there is a
powerful cultural imperative to perform traditional Zulu songs. These songs
are not entertainment — they express joy, celebrate the union of two families,
and honour the ancestors. Failing to incorporate traditional music is a
serious departure from cultural norms.

The music takes the form of a friendly, celebratory singing and dancing
competition between the two families. Both families must have rehearsed
their repertoires before the wedding day.

Vocal styles:
- Isicathamiya — harmonious a cappella singing by groups of men
  (made famous by Ladysmith Black Mambazo)
- Imbube — a more powerful a cappella style that may feature a female soprano
- Call-and-response chants — backbone of all participatory wedding music

Traditional songs:
- "Baba Nomama" — classic traditional wedding song
- "Ndihamba Nawe" by Mafikizolo — contemporary favourite, wedding staple
- "Vuli Ndlela" by Brenda Fassie — announces the arrival of the bride
- "Sivulele" and "Amavolovolo" — traditional songs frequently featured

Traditional dances:
- Indlamu — powerful, energetic men's dance with high kicks, stomping,
  shields and sticks; showcases strength; centrepiece of the celebration
- Isizingili (Ingoma) — performed by young men and women; high kicks,
  singing, and clapping; specifically associated with weddings

5 ceremony music moments:
1. Ukufika Kwabakamakoti (arrival of bride's family) — bride's family arrives
   singing their own traditional songs; groom's family responds with theirs.
   A musical exchange. This IS the ceremony opening.
2. Unveiling of the bride — always accompanied by a special song speaking of
   the bride's beauty or her new journey
3. Ukwaba (gift-giving ceremony) — each act of giving accompanied by a song
   and dance; recipient dances joyfully; family sings appreciation
4. Ukuhlatshwa Kwenkomo (slaughtering of the cow) — solemn, sacred moment;
   specific chants directed towards the ancestors
5. Ukugida Komakoti (bride's dance) — bride performs a special dance for her
   new family; groom and his family join in

Traditional instruments:
- Ingungu — traditional drum made of wood and animal skin
- Whistles and horns — signal key moments
- Guitar, violin, concertina (ikostina) — maskandi style for more
  contemporary celebrations

Amplification: not used during core traditional rituals.
Acceptable for reception or less formal segments.

Most forgotten: underestimating the importance of family participation,
neglecting the traditional songbook in favour of contemporary hits,
poor coordination and rehearsal between families, not involving elders
who are custodians of the traditional repertoire.

SA cultural note: Many Zulu couples have both an umabo and a church wedding.
Plan both separately — they are entirely different musical worlds. Couples
who pour all energy into the church wedding and treat the umabo as an
afterthought miss a profoundly meaningful celebration.
*/

// ─────────────────────────────────────────────────────────────────────────────
// XHOSA TRADITIONAL WEDDINGS (UMTSHATO)
// ─────────────────────────────────────────────────────────────────────────────
/*
Same fundamental planning frame as Zulu: the music is the families.
No formal religious prohibitions. The music is expected to be respectful
and appropriate. A distinctive feature: Xhosa wedding songs have
DIDACTIC and PEDAGOGICAL content — lyrics often contain guidance on
married life, family values, and responsibilities. Songs with frivolous
themes are discouraged.

Call-and-response singing is the backbone — designed to involve everyone.
Failing to encourage community participation leads to a less vibrant
celebration. Elders must lead song selection — they are the custodians
of the traditional repertoire.

Traditional songs:
- "Qongqothwane" (The Click Song) — made internationally known by Miriam Makeba.
  Sung to bring good fortune. VIRTUALLY OBLIGATORY at a Xhosa wedding.
- "Somagwaza" — traditional song integral to Xhosa ceremonies
- "Umtshotsho" — particularly suited for women's voices; commonly performed
- Iingoma zesiNtu — traditional songs with cyclical, repetitive structures
  and two-chord harmonic progressions; tell stories, recount ancestral wisdom

Traditional instruments:
- Uhadi — calabash-resonated musical bow; plays both melody and harmony;
  prominent in Xhosa music
- Umrhubhe — mouth bow that reinforces the lead singer's melody with overtones
- Ingqongqo — rudimentary drum made from ox hide
- Ikawu — shield-like drum for rhythmic accompaniment

Ceremony is primarily unamplified — relies on the natural acoustics of
massed voices. Modern instruments not forbidden but traditional instruments
strongly encouraged for cultural authenticity.

4 ceremony music moments:
1. Uduli (arrival of bride's party at groom's home) — songs exchanged between
   the two families; a musical dialogue that opens the ceremony
2. Umdudo (main celebration) — vibrant dancing and singing; bride formally
   presented to groom's family with specific songs and dances
3. Ukuyala (counselling of the bride by elders) — more solemn; subdued music
   or none
4. Ukuxhentsa (feast) — continuous singing, dancing, and traditional music

Most forgotten: neglecting community participation (call-and-response must
involve everyone), not involving elders in song selection, failing to rehearse
call-and-response songs with families before the day, underestimating the
length and complexity of the full traditional ceremony.
*/

// ─────────────────────────────────────────────────────────────────────────────
// CAPE MALAY WEDDINGS
// ─────────────────────────────────────────────────────────────────────────────
/*
CRITICAL: Same Nikah rule as mainstream Muslim — the religious ceremony has
NO music. Do not propose music for the Nikah. The music planner's role
begins only at the Walima.

Where Cape Malay diverges significantly from mainstream Muslim practice:
the Walima (reception) has a rich, specific, and culturally expected
music tradition unlike any other SA tradition. Families have their own
favourite songs and ignoring these expectations — particularly those of
the older generation — can cause genuine hurt.

The Cape Malay choir is the centrepiece: a soloist sings in quarter tones
(a remnant of Eastern musical influences) while the choir provides harmonies
in a more Western tonal scale. This is a distinctive and beautiful sound
that cannot be replicated by a generic function band.

Traditional songs (strongly expected by families):
- "Rosa" — beloved traditional Cape Malay love song. Its inclusion is
  VIRTUALLY EXPECTED. Not including it will be noticed.
- "Caravans" by Mike Batt — traditional couple's entrance song at the
  reception. Use has declined among younger couples but many families
  still expect it — discuss with the couple AND their parents.
- Ghoemaliedjies — lively, rhythmic "picnic songs" named after the ghoema drum
- Moppies — comic, lighthearted songs that add a festive element
- Dutch folk songs — adapted into Cape Malay repertoire; reflects historical ties

Traditional instruments:
- Ghoema — small hand-held drum of Malay origin; rhythmic heart of the music
- Guitar, banjo, and mandolin — melody and harmony
- Cello — rich bass line
- Violin and double bass — in larger ensembles

Acoustic tradition — heavily amplified or electronic instruments are a
departure from the traditional sound and may be seen as out of place.

Cape Malay wedding events and music:
1. Mendhi night (pre-wedding) — ratib drumming, singing, celebration
2. Nikah (religious ceremony) — NO MUSIC
3. Walima entrance of couple — "Caravans" or another significant song
4. Walima feast — Cape Malay choir performs; family members sing;
   ghoemaliedjies and moppies throughout
5. Bruidskamer (couple's new home, post-reception) — informal singing
   and celebration may continue

Duration note: Cape Malay weddings can stretch over 12 hours.
Plan sufficient musical content for the full duration of the Walima.
Ensure the choir or band has the stamina and repertoire for a long celebration.

If hiring professionals: a professional Cape Malay choir or band is essential
for an authentic experience. A generic wedding band unfamiliar with the
Cape Malay repertoire will not serve this wedding well.

Family singing tradition: it is cherished for family members (especially
uncles) to take the microphone and sing their favourite songs.
Plan for this. Do not fight it.

Most forgotten: incorporating "Rosa" and "Caravans" for elders, maintaining
the Nikah as entirely non-musical, hiring Cape Malay specialists rather
than generic bands, planning the Mendhi night music separately.
*/

// ─────────────────────────────────────────────────────────────────────────────
// AFRIKANER WEDDINGS (NG KERK / DUTCH REFORMED)
// ─────────────────────────────────────────────────────────────────────────────
/*
Most musically conservative tradition in the framework.
Dominee controls everything. Pipe organ is the primary and often sole
instrument. No contemporary music, bands, DJs, electric guitars, synths,
or drum kits in the ceremony.

All music from the Liedboek van die Kerk (official church hymnal).
Structure follows the official Huweliksformulier.
Any departure from approved hymns requires dominee approval — do not
assume personal songs can be introduced.

Popular hymn choices:
- "Op Vaste Fondamente" (On a Firm Foundation)
- "Lofsing die Here" (Praise the Lord)
- Lied 290 and other hymns from the Liedboek on themes of love and commitment

String quartet, solo violinist, or piano are generally permissible alongside
the organ if discussed with the church in advance. Confirm with the church
whether any non-organ instruments are permitted.

Couples must arrange a rehearsal with the organist well in advance.
Ensure chosen pieces work within the acoustic and instrumental constraints
of the church — a song relying on a beat or full band will not translate
to solo organ.

5 ceremony moments:
1. Prelude — organ music as guests arrive; classical or sacred pieces (15–30 min)
2. Processional — traditional wedding march or dominee-approved alternative
3. Congregational hymns (2–3) — sung by all guests; from the Liedboek
4. Signing of the register — SA legal requirement; instrumental or organ solo
5. Recessional — triumphant, joyous organ piece

Ceremony and reception are COMPLETELY SEPARATE musical worlds.
The reception (often held at a venue, not the church) has considerably
more freedom — popular Afrikaans artists (Steve Hofmeyr, Bok van Blerk,
Kurt Darren) and international music are common.
Planner must be briefed on both separately.

Most forgotten: selecting inappropriate processional music, failing to
consult the dominee early enough, not arranging organist rehearsal,
not considering whether chosen pieces suit organ arrangement.
*/

