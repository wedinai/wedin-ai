// HINDU WEDDINGS (KZN INDIAN COMMUNITY)
// ─────────────────────────────────────────────────────────────────────────────
/*
SA context: Tamil/South Indian (Carnatic tradition, Nadaswaram) vs North Indian/
Gujarati (Bollywood, dhol) — ESTABLISH THIS FIRST. Recommendations differ
significantly between these two streams.

All ceremony music must be devotional and auspicious. Film song melodies are
acceptable if the LYRICS are devotional — this is a well-established SA
practice since the 1960s. Content must be sacred even if the melody is borrowed.

Pre-wedding ceremonies (often as musically complex as the wedding itself):
- Ganesh Puja
- Sangeet — frequently underestimated; plan separately with full musical brief
- Mehndi
- Haldi

8 ceremony moments:
1. Baraat (groom's procession) — shehnai (North Indian) or nagasvaram (South Indian/
   Tamil) leads the groom into the hall. This is culturally expected and should be
   live. Finding trained shehnai/nagasvaram players in SA is challenging — flag
   early, minimum 3 months lead time. Dhol player non-negotiable for North Indian.
2. Groom's entry to mandap — festive and celebratory
3. Varmala/Jai Mala — garland exchange
4. Bride's entry — devotional piece accompanies the bride
5. Pheras/Saptapadi (fire ceremony and seven steps) — pandit chants; background
   music should be minimal or absent. Musician follows the pandit's pace, not
   the other way around. Brief the act on this explicitly.
6. Kanyadaan (giving away of the bride)
7. Sindoor/Mangalsutra/Thali — the central ritual; minimal music
8. Mangalam — THE MOST IMPORTANT MUSIC MOMENT. Performed at the conclusion of
   the ceremony. A song of blessing and praise. Non-negotiable — even couples
   who want a modern ceremony must include the mangalam. Omitting it would be
   noticed by older family members and is considered inauspicious.
   Mangalam melodies may be based on classical ragas or adapted from folk/film.
9. Vidaai (bride's farewell) — intensely emotional moment, frequently left
   unplanned. Must be planned. A song that honours the gravity of the moment.

Traditional instruments:
- Shehnai/nagasvaram: ceremonial wind instruments for the Baraat
- Harmonium and tabla: common in South Indian ceremonies
- Dholak: hand drum, accompanied women's singing in traditional ceremonies
- Mrdangam and violin: more typical of North Indian ceremonies

Amplified music permitted but must not drown out the pandit's chanting.

Most forgotten: Baraat planned separately, Vidaai, briefing DJ on pandit's pacing,
underestimating the Sangeet, finding shehnai/nagasvaram players early enough.
*/

// ─────────────────────────────────────────────────────────────────────────────
// CATHOLIC WEDDINGS
// ─────────────────────────────────────────────────────────────────────────────
/*
Most formally codified music rules of any tradition. All music during the
liturgy MUST be sacred in character. Secular love songs, film themes, pop
ballads, and operatic arias are STRICTLY FORBIDDEN within the ceremony
regardless of personal meaning. These are appropriate for the reception only.

CRITICAL: Confirm Full Nuptial Mass vs Rite of Marriage with priest first.
Structure and required sung elements differ. Couple does not have freedom
to substitute freely — priest specifies what is fixed and what can be chosen.

SA inculturation note: The style can reflect local culture even though the
type must be sacred. Traditional Zulu hymns, amakorasi (choruses), and
indigenous sacred songs in isiZulu, Sesotho, or isiXhosa are not only
permitted but encouraged in KZN and Gauteng parishes.

Required sung elements (cannot be replaced with personal songs):
Sanctus, Memorial Acclamation, Great Amen, Agnus Dei, Responsorial Psalm

9 ceremony moments:
1. Prelude — organ or sacred instrumental as guests arrive (15–30 min — most
   frequently unplanned)
2. Processional — hymn or inculturated processional. SA example:
   "Masi siguqe sengena Inkosi" (call-and-response entrance hymn with drums and dance)
3. Gloria — sung by congregation; may be performed as "Ihubo leNkosi" in
   inculturated liturgies
4. Responsorial Psalm — set to music; congregation sings the response
5. Gospel Acclamation — Alleluia or seasonal acclamation, sung
6. Offertory Procession — amakorasi or hymn; may include ring dance in
   inculturated liturgies
7. Sanctus — sung by congregation; may include ukugiya dance
8. Communion — hymn or reflective music while congregation receives
9. Signing of the register — SA legal requirement; common moment for Ave Maria.
   NEAR-UNIVERSAL and FREQUENTLY UNPLANNED — flag in every Catholic brief.
10. Recessional — joyful hymn with dance; mirrors the energy of the entrance

Instrument restrictions: organ is primary. Strings, brass, woodwinds at
pastor's discretion. No electric guitars, synths, or drum kits in traditional
parishes. Indigenous instruments (idlamu drum, marimba) welcome in inculturated
liturgies — confirm with priest.

Music should facilitate congregational participation — not a solo performance
for passive listening. Ensure at least some hymns are known enough for guests
to sing.

Most forgotten: Ave Maria/flowers moment, required Mass elements cannot be
swapped for personal songs, meeting parish music director early enough.
Couples who select secular songs often discover the rejection at the final
rehearsal — avoid this entirely by establishing sacred-only requirement upfront.
*/

// ─────────────────────────────────────────────────────────────────────────────
// PROTESTANT AND NON-DENOMINATIONAL CHRISTIAN WEDDINGS
// ─────────────────────────────────────────────────────────────────────────────
/*
Significantly more flexibility than Catholic but varies enormously by
denomination and congregation. Wedding is a worship service — secular music
almost universally reserved for reception.

NEVER assume complete freedom without checking with the minister first.
Even relaxed churches may have firm views on specific songs.

Denominational variations:
- Anglican: formal, organ primary, traditional hymns, vicar must approve all music
- Methodist: hymnal-based, congregational singing focus, live preferred over recorded
- Baptist: more openness to contemporary Christian/gospel, unity ceremonies common
- Pentecostal/Charismatic: full worship band, contemporary worship, praise section
  is a central ceremony element — planners must plan for full band logistics and
  sound requirements

Common SA choices:
- Classic hymns: "How Great Thou Art", "Great Is Thy Faithfulness", "Amazing Grace"
- Contemporary worship: "10,000 Reasons" (Matt Redman), "Goodness of God",
  "Here I Am to Worship"
- SA gospel: Joyous Celebration, Benjamin Dube, Sipho Makhabane.
  Call-and-response singing is common and culturally expected in many Black
  SA congregations.
- Hymns in indigenous languages: singing in isiZulu, isiXhosa, Sesotho, or
  Setswana alongside English is a cherished practice — encourages linguistic
  inclusivity across the guest list.

6 standard ceremony moments:
1. Prelude — instrumental or quiet vocal as guests arrive (15–30 min)
2. Seating of parents/grandmothers — distinct cue, frequently overlooked.
   Must be planned and briefed.
3. Processional — distinct piece for bridal party, then distinct piece for bride
4. Opening hymn or worship song — congregational singing after welcome or prayer
5. Signing of the register — SA legal requirement; solo, duet, or instrumental
6. Recessional — joyful, uplifting

Most forgotten: checking with minister before finalising any song list, planning
for linguistic diversity of the guest list, relying entirely on pre-recorded
tracks (loses flexibility for real-time timing adjustments).
*/

